Map<String, String> _parseHindiMap() {
  final map = <String, String>{};
  for (final line in const LineSplitter().convert(_hindiRaw)) {
    if (line.trim().isEmpty) continue;
    final sep = line.indexOf('|');
    if (sep <= 0) continue;
    map[line.substring(0, sep).trim()] = _repairMojibake(line.substring(sep + 1).trim());
  }
  return map;
}

String _repairMojibake(String value) {
  if (value.isEmpty) {
    return value;
  }
  if (!value.contains('à¤') && !value.contains('à¥')) {
    return value;
  }

  try {
    return utf8.decode(latin1.encode(value));
  } catch (_) {
    return value;
  }
}

void _applyHindi(File file, Map<String, String> hindiMap) {
  final decoded = json.decode(file.readAsStringSync()) as List<dynamic>;
  for (final item in decoded.whereType<Map<String, dynamic>>()) {
    final key = '${item['chapter']}:${item['verse_number']}';
    item['translation_hindi'] = hindiMap[key] ?? 'हिंदी अनुवाद उपलब्ध नहीं है।';
  }

  final encoder = const JsonEncoder.withIndent('    ');
  file.writeAsStringSync(encoder.convert(decoded));
}

void main() {
  final hindiMap = _parseHindiMap();
  _applyHindi(File('assets/data/verses.json'), hindiMap);
  _applyHindi(File('assets/data/verse.json'), hindiMap);
  stdout.writeln('UPDATED_HINDI_TRANSLATIONS=${hindiMap.length}');
}
